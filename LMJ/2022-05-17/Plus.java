public class Plus{
	public static void main(String[] args) {
		// method(메소드) call (호출)
		System.out.println(10 + "10"); //"1010" 이라는 문자열 출력
		System.out.println("20" + 20);
		System.out.println();
		System.out.println(5.123);
		System.out.println(0.1+0.1);

		System.out.print(1);
		System.out.print(2);
		System.out.print(3);
	}
}