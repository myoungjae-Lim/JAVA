//두 수 44와 72의 + - * / 각각의 연산결과를 출력해보세요.

public class Calc{
	public static void main(String args[]){
		int x = 44;
		int y = 72;

		System.out.println("두수의 합: " + (x+y));
		System.out.println("두수의 차: " + (x-y));
		System.out.println("두수의 곱: " + (x*y));
		System.out.println("두수의 몫: " + (x/y));
	}
}